use advent_of_code::ElfCollection;
use std::fs;

fn main() {
    let mut elves = ElfCollection::new();

    let contents = fs::read_to_string("input.txt").expect("unable to read input file");

    for line in contents.lines() {
        elves.add(line);
    }

    println!("most calories: {}", elves.biggest());

    let top_three = elves.top_three();
    let top_three_total: u32 = top_three.iter().sum();

    println!("top three total: {}", top_three_total);
}
